package com.javaLearn.p7_scale;

public class ScaleDemo01 {
    /*
        计算机中的进制分类：

            十进制 : 默认格式
            二进制 : 0b 为开头
            八进制 : 0 为开头
            十六进制 : 0x 为开头
     */
    public static void main(String[] args) {

        System.out.println(110);    // 十进制
        System.out.println(0b110);  // 二进制 0b开头
        System.out.println(0110);   // 八进制 0开头
        System.out.println(0x110);  // 十六进制 0x开头
    }
}
