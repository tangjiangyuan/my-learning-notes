package com.javaLearn.p1_variable;

public class VariableDemo01 {
    /*
        变量介绍：内存中的存储空间，空间中记录着经常发生改变的数据

        变量定义格式：

                数据类型 变量名 = 数据值;

                数据类型：整数：int

     */
    public static void main(String[] args) {
        int salary = 15000;
        System.out.println("工资是："+salary);
    }
}
