package com.javalearn.p3_loop;

public class DoWhileTest {
    public static void main(String[] args) {
        doWhileTest();
    }

    // do...while 循环
    public static void doWhileTest() {
        int i = 1;
        do {
            System.out.println("i love u");
            i++;
        } while (i <= 5);
    }
}
