package com.javalearn.p1_operator;

public class OperatorDemo02 {
    /*
        关系运算符：
            ==
            !=
            >
            >=
            <
            <=
     */
    public static void main(String[] args) {

        System.out.println(10 == 20);
        System.out.println(10 != 20);
        System.out.println(10 > 20);
        System.out.println(10 >= 20);
        System.out.println(10 < 20);
        System.out.println(10 <= 20);
    }
}
